    private void jcbAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbAlunoActionPerformed
        int alunoSelecionado = (jcbAluno.getSelectedIndex() + 1);
        AlunoController aluno = new AlunoController(this.dbName);
        jcbTreino.removeAllItems();
        this.codigoTreino.clear();
        try {
            aluno.conectar();
            
            ResultSet rset = aluno.getTreinoAluno(alunoSelecionado);
            while(rset.next()){
                jcbTreino.addItem(rset.getString("treino_nome"));
                this.codigoTreino.add(rset.getInt("codigo_treino"));
            }
            aluno.desconectar();
        } catch (Exception ex) {
            Logger.getLogger(CadastroTreino.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jcbAlunoActionPerformed

