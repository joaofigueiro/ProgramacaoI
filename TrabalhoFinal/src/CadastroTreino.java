    private void jcbTreinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbTreinoActionPerformed
        try {
            if (this.codigoTreino.size() > 0) {
                jcbExercicio.removeAllItems();
                int treinoSelecionado = jcbTreino.getSelectedIndex();                
                ExercicioController exerc = new ExercicioController(this.dbName);
                exerc.conectar();

                ResultSet rset = exerc.getExerc(this.codigoTreino.get(treinoSelecionado));
                while(rset.next()) {
                    jcbExercicio.addItem(rset.getString("nome_exercicio"));
                }

                exerc.desconectar();
            }
        } catch (Exception ex) {
            Logger.getLogger(CadastroTreino.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jcbTreinoActionPerformed

